// switch case in c++
#include <bits/stdc++.h>
using namespace std;
int main()
{
    int num = 2;

    cout << endl;
    switch (num)
    {
    case 1:
        cout << "First" << endl;
        break;

    case 2:
        cout << "Second" << endl;
        break;

    default:
        cout << "It is the default case" << endl;
        break;
    }

    cout << endl;
    return 0;
}