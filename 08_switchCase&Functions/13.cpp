// functon to print counting from 1 to n
#include <bits/stdc++.h>
using namespace std;

void printCounting(int n)
{
    // function body
    for (int i = 1; i <= n; i++)
    {
        cout << i << endl;
    }
}
int main()
{
    int n;
    cin >> n;
    // function call
    printCounting(n);
}